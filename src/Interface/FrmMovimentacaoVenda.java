    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        GerenciarProdutos gr = new GerenciarProdutos();
        int venderPCodigo = Integer.parseInt(lCodigo.getText());
        int venderPQuantidade = Integer.parseInt(txtValor.getText());
        int estoquePQuantidade = Integer.parseInt(lQuantidade.getText());
        double venderPValorUnit = Double.parseDouble(lPreco.getText());
        
        double valorFinal = venderPQuantidade * venderPValorUnit;
        
        try {
            if(estoquePQuantidade > venderPQuantidade){
                gr.venderProduto(venderPCodigo, venderPQuantidade);
                JOptionPane.showMessageDialog(null, "Venda realizada com sucesso, valor total : " + valorFinal, "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
                carregarTabela();
                limpaLabels();
            } else {
                  JOptionPane.showMessageDialog(null, "verifique a quantidade de produtos selecionados e a disponibilidade em estoque.\nPedido com quantidade acima do nosso estoque.", "ERROR", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Verifique a disponibilidade do produto.", "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnVenderActionPerformed

    private void tabelaProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProdutosMouseClicked
        int index = tabelaProdutos.getSelectedRow();
        GerenciarProdutos gr = new GerenciarProdutos();
        ModeloTabelaProdutos modelo = new ModeloTabelaProdutos(gr.getProdutos());

        int codigo = Integer.parseInt(modelo.getValueAt(index, 0).toString());
        String nome = modelo.getValueAt(index, 1).toString();
        String fabricante = modelo.getValueAt(index, 2).toString();
        int quantidade = Integer.parseInt(modelo.getValueAt(index, 3).toString());
        double preco = Double.parseDouble(modelo.getValueAt(index, 4).toString());

        lCodigo.setText(Integer.toString(codigo));
        lNome.setText(nome);
        lFabricante.setText(fabricante);
        lQuantidade.setText(Integer.toString(quantidade));
        lPreco.setText(Double.toString(preco));
    }//GEN-LAST:event_tabelaProdutosMouseClicked

    private void carregarTabela(){
        GerenciarProdutos gr = new GerenciarProdutos();
        
        ModeloTabelaProdutos modelo = new ModeloTabelaProdutos(gr.getProdutos());
        
        tabelaProdutos.setModel(modelo);
    }
    
