    private void cbTipoProdutoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbTipoProdutoItemStateChanged
        if(cbTipoProduto.getSelectedIndex() == 1 ){
            pnEletronico.setVisible(true);
            pnPerecivel.setVisible(false);
            pnMovel.setVisible(false);
        }
        if(cbTipoProduto.getSelectedIndex() == 2 ){
            pnMovel.setVisible(true);
            pnEletronico.setVisible(false);
            pnPerecivel.setVisible(false);
        }
        if(cbTipoProduto.getSelectedIndex() == 3 ){
            pnPerecivel.setVisible(true);
            pnEletronico.setVisible(false);
            pnMovel.setVisible(false);
        }
    }//GEN-LAST:event_cbTipoProdutoItemStateChanged

    public void limpaTudo(){
        txtNomeProduto.setText("");
        txtFabricanteProduto.setText("");
        txtQuantidadeProduto.setText("");
        txtPrecoProduto.setText("");
        cbTipoProduto.setSelectedIndex(0);
        pnEletronico.setVisible(false);
        cbVoltagem.setSelectedIndex(0);
        txtPotencia.setText("");
        cbEficiencia.setSelectedIndex(0);
        pnPerecivel.setVisible(false);
        dtValidade.setText("  /  /    ");
        cbRefrigerado.setSelected(false);
        pnMovel.setVisible(false);
        txtMaterialTipo.setText("");
    }

