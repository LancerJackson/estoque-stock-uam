    private void btAvancarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAvancarActionPerformed
        GerenciarProdutos gr = new GerenciarProdutos();
        try {
            int codigoP = gr.tamanhoLista()+1;
            String nomeP = txtNomeProduto.getText();
            String fabricanteP = txtFabricanteProduto.getText();
            int quantidadeP = Integer.parseInt(txtQuantidadeProduto.getText());
            double precoP = Double.parseDouble(txtPrecoProduto.getText());

            if(cbTipoProduto.getSelectedIndex() == 1 ){
                int tipoCodigoP = 1;
                String voltagemP = cbVoltagem.getSelectedItem().toString();
                String potenciaP = txtPotencia.getText();
                String stringEficiencia = cbEficiencia.getSelectedItem().toString();
                char eficienciaP = stringEficiencia.charAt(0);

                Produto p = new Eletronico(codigoP, nomeP, fabricanteP, quantidadeP, precoP, tipoCodigoP, voltagemP, potenciaP, eficienciaP);
                gr.adicionar(p);
                limpaTudo();

                JOptionPane.showMessageDialog(null,"Produto Eletronico cadastrado com sucesso.","SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            } 
            if(cbTipoProduto.getSelectedIndex() == 2 ){
                int tipoCodigoP = 2;
                String materialP = txtMaterialTipo.getText();

                Produto p = new Movel(codigoP, nomeP, fabricanteP, quantidadeP, precoP, tipoCodigoP, materialP);
                gr.adicionar(p);
                limpaTudo();           

                JOptionPane.showMessageDialog(null,"Produto Movel cadastrado com sucesso.","SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            } 
            if(cbTipoProduto.getSelectedIndex() == 3 ){
                int tipoCodigoP = 3;
                boolean refrigeradoP;
                if(cbRefrigerado.isSelected()){
                    refrigeradoP = true;
                } else {
                    refrigeradoP = false;
                }

                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("d/MM/yyyy");
                String validadeString = dtValidade.getText();
                LocalDate validadeP = LocalDate.parse(validadeString, formatter);

                Produto p = new Perecivel(codigoP, nomeP, fabricanteP, quantidadeP, precoP, tipoCodigoP, validadeP, refrigeradoP);
                gr.adicionar(p);
                limpaTudo();
                JOptionPane.showMessageDialog(null,"Produto Perecivel cadastrado com sucesso.","SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            if(cbTipoProduto.getSelectedIndex() == 0){
                JOptionPane.showMessageDialog(null,"TIPO DE PRODUTO NÃO SELECIONADO.","ERROR", JOptionPane.WARNING_MESSAGE);
            }else {
                JOptionPane.showMessageDialog(null, "CAMPO EM BRANCO OU DADOS DIGITADOS INCORRETOS.", "ERROR", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btAvancarActionPerformed

